const GROUP_ORDER = ["Authentication", "Headers", "Routing", "DNS", "Format", "Threats", "Infrastructure"];
const GROUP_COLOR = { Authentication: T.accent, Headers: T.info, Routing: T.good, DNS: "#8FE1B8", Format: T.dim, Threats: T.bad, Infrastructure: T.accent2 };

export function GlossaryPage() {
  const [q, setQ] = useState("");
  const filtered = useMemo(() => {
    const s = q.trim().toLowerCase();
    if (!s) return TERMS;
    return TERMS.filter((x) => x.t.toLowerCase().includes(s) || x.d.toLowerCase().includes(s));
  }, [q]);

  const grouped = useMemo(() => {
    const m = new Map();
    for (const term of filtered) {
      if (!m.has(term.g)) m.set(term.g, []);
      m.get(term.g).push(term);
    }
    return m;
  }, [filtered]);

  return (
    <PageShell
      eyebrow="REFERENCE"
      title="Email security glossary"
      subtitle="Plain-language definitions for every technical term used throughout the analyzer — no prior email-authentication background required."
      wide
    >
      <div style={{ marginBottom: 26 }}>
        <input
          value={q}
          onChange={(e) => setQ(e.target.value)}
          placeholder="Search terms — try “alignment” or “rDNS”…"
          style={{
            width: "100%",
            maxWidth: 420,
            background: T.panel2,
            border: `1px solid ${T.line}`,
            borderRadius: T.r2,
            color: T.ink,
            padding: "11px 16px",
            fontSize: 14,
            fontFamily: T.body,
          }}
        />
        <span style={{ marginLeft: 12, fontSize: 12.5, color: T.faint, fontFamily: T.mono }}>
          {filtered.length} of {TERMS.length} terms
        </span>
      </div>

      {GROUP_ORDER.filter((g) => grouped.has(g)).map((g) => (
        <div key={g} style={{ marginBottom: 26 }}>
          <div
            style={{
              display: "inline-block",
              fontFamily: T.mono,
              fontSize: 11.5,
              fontWeight: 700,
              letterSpacing: 0.8,
              color: GROUP_COLOR[g],
              border: `1px solid ${GROUP_COLOR[g]}44`,
              background: GROUP_COLOR[g] + "14",
              borderRadius: 999,
              padding: "4px 12px",
              marginBottom: 12,
            }}
          >
            {g.toUpperCase()}
          </div>
          <div style={{ display: "grid", gridTemplateColumns: "repeat(auto-fill, minmax(340px, 1fr))", gap: 12 }}>
            {grouped.get(g).map((term) => (
              <div
                key={term.t}
                style={{
                  background: T.panel,
                  border: `1px solid ${T.line}`,
                  borderRadius: T.r2,
                  padding: "16px 18px",
                  boxShadow: T.shadowSm,
                }}
              >
                <div style={{ fontWeight: 700, color: T.ink, fontSize: 14.5, marginBottom: 6 }}>{term.t}</div>
                <div style={{ fontSize: 13, color: T.dim, lineHeight: 1.6 }}>{term.d}</div>
              </div>
            ))}
          </div>
        </div>
      ))}

      {filtered.length === 0 && (
        <div style={{ color: T.faint, fontSize: 14, padding: "30px 0", textAlign: "center" }}>
          No terms match “{q}”.
        </div>
      )}
    </PageShell>
  );
}
